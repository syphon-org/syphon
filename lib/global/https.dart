/// From dart:io, create a HttpClient with a trusted certificate [cert]
/// added to SecurityContext.
/// Wrapped in try catch in case the certificate is already trusted by
/// device/os, which will cause an exception to be thrown.
HttpClient customHttpClient({String? cert}) {
  final SecurityContext context = SecurityContext.defaultContext;

  try {
    if (cert != null) {
      context.setTrustedCertificatesBytes(utf8.encode(cert));
    }
  } on TlsException catch (e) {
    if (e.osError?.message != null && e.osError!.message.contains('CERT_ALREADY_IN_HASH_TABLE')) {
      log.info('[customHttpClient] - cert already trusted! Skipping.');
    } else {
      log.error('[customHttpClient] setTrustedCertificateBytes EXCEPTION: $e');
      rethrow;
    }
  } finally {}

  return HttpClient(context: context);
}

/// Use package:http Client with our custom dart:io HttpClient with added
/// LetsEncrypt trusted certificate
http.Client createClient({ProxySettings? proxySettings}) {
  final HttpClient customClient = customHttpClient(cert: ISRG_X1);

  customClient.findProxy = (uri) {
    if (proxySettings != null && proxySettings.enabled) {
      // Adding credentials if the proxy authentication is enabled
      if (proxySettings.authenticationEnabled) {
        customClient.addProxyCredentials(
            proxySettings.host,
            int.parse(proxySettings.port), // port input allows numbers only, so no try/catch
            'Basic', // Basic authentication
            HttpClientBasicCredentials(proxySettings.username, proxySettings.password));
      }

      return 'PROXY ${proxySettings.host}:${proxySettings.port};';
    } else {
      return 'DIRECT';
    }
  };

  return IOClient(customClient);
}

